#!/usr/bin/env bash
# build-css.sh — compose css/site.css from sources.
#
# Reads:
#   css/sources/_imports.css                 — @import statements
#   css/sources/_layout.css                  — block kinds, button, footer
#   plugins/myst-mermaid/css/mermaid.css     — mermaid plugin CSS
#
# Writes:
#   css/site.css                             — concatenated, with @imports
#                                              bubbled to the top
#
# Re-run any time a source changes. The output is committed alongside the
# sources.

set -euo pipefail

TOOLKIT_ROOT="$(cd "$(dirname "${BASH_SOURCE[0]}")/.." && pwd)"
cd "$TOOLKIT_ROOT"

# Sources, in the order their non-@import content should appear.
SOURCES=(
  "css/sources/_imports.css"
  "css/sources/_layout.css"
  "plugins/myst-mermaid/css/mermaid.css"
)

# Sanity check.
for s in "${SOURCES[@]}"; do
  [ -f "$s" ] || { echo "❌ missing source: $s" >&2; exit 1; }
done

OUT="css/site.css"
TMP=$(mktemp)

{
  # 1. Banner.
  cat <<'EOF'
/* myst-docs-toolkit — composed site stylesheet.
 *
 *   GENERATED by bin/build-css.sh. Do not edit by hand — your changes
 *   will be overwritten the next time the script runs.
 *
 * Sources:
 *   css/sources/_imports.css                  — @import statements
 *   css/sources/_layout.css                   — layout, button, footer
 *   plugins/myst-mermaid/css/mermaid.css      — mermaid plugin CSS
 *
 * Re-build with:   ./bin/build-css.sh
 */

EOF

  # 2. Every @import statement from every source, in source order,
  #    deduped (keeping the first occurrence). Browsers require @import
  #    to come before any other rule. Sources can each have their own
  #    @imports (so they remain usable standalone); dedupe here so the
  #    composed file doesn't double-load fonts etc.
  for s in "${SOURCES[@]}"; do
    grep -E '^[[:space:]]*@import' "$s" || true
  done | awk '!seen[$0]++'

  # 3. A blank line between imports and rules for readability.
  echo ""

  # 4. The rules: every line that is NOT an @import, from each source
  #    in the declared order, with the file name as a section header.
  for s in "${SOURCES[@]}"; do
    echo "/* ===== from $s ===== */"
    grep -vE '^[[:space:]]*@import' "$s"
    echo ""
  done
} > "$TMP"

# Move into place atomically.
mv "$TMP" "$OUT"

echo "✅ composed $OUT ($(wc -l < "$OUT") lines)"
